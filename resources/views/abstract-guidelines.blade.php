<h2>ABOUT THE REVIEWING PROCESS </h2>

<p>Your abstract will undergo a double-blind peer review by the Scientific committee within two to three weeks after the last date of submission.All abstracts will be reviewed using four criteria: </p>

<ul>
<li> Originality </li>
<li>Clarity</li>
<li>Rigour and</li>
<li>Practical relevance. </li>

</ul>

<p>

These reviews will be passed on to the Organising Committee, which will make a final decision on which abstracts to accept and in what categories. </p>
<p>
Results of the abstract review will be sent to the author within two weeks of submission </p>
<p>
The Conference Chair serves as the head of the scientific committee and will take the final decision on abstracts. </p>

<p>
Acceptance or rejections of the paper will be sent to you with reviewer comments. </p>

<p>
If a revision is required, the revised abstract must be sent back within a week
If your abstract is accepted, only if you are registered. </p>


