<h1 style="color: #ba1617;">History of INPALMS:</h1>

<div class="row">
    <div class="col-md-7">
        <p style="text-align: justify;">INPALMS is forty-two years old today. The first meeting of the Asia Pacific Association of Law, Medicine, and Science was held in Singapore from 18th to 22nd September 1983 with the participation of 400 delegates from 30 countries attending the meeting. The formation of the medico-legal assembly in our region was the brainchild of Prof. Choe Tzee Cheng, foundation president of the organisation [Singapore] together with Prof. MSL Salgado [Sri Lanka] and Prof. Widyathmaka [Indonesia]. At the post-congress Annual General Meeting in Singapore, it was decided to change the name of the Association to the Indo- Pacific Association of Law, Medicine and Science (INPALMS) to include the African countries, highlighting the true geographical scope of the organization. The next congress of the association under the title of INPALMS was held in Colombo in 1986 and since then, INPALMS entertained regular international congresses every three years.</p>
        <p style="text-align: justify;">The organization holds conferences and congresses where experts share research, case studies, and best practices in forensic medicine, medical jurisprudence, and related disciplines. These events provide a platform for networking and collaboration among professionals in law, medicine, and forensic sciences.</p>
